package au.edu.monash.merc.capture.domain;

import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;
import javax.persistence.TableGenerator;

@Entity
@Table(name = "ref_site")
public class ReferenceSite extends Domain {

	@Id
	@GeneratedValue(strategy = GenerationType.TABLE, generator = "pk_generator")
	@TableGenerator(name = "pk_generator", pkColumnName = "pk_column_name", valueColumnName = "pk_column_value", pkColumnValue = "ref_site_pk")
	private long id;

	@Basic
	@Column(name = "name", length = 255)
	private String name;

	@Basic
	@Column(name = "url_link", length = 255)
	private String url;

	@Basic
	@Column(name = "description", length = 255)
	private String description;

	@Basic
	@Column(name = "logo_name", length = 100)
	private String logoName;

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getLogoName() {
		return logoName;
	}

	public void setLogoName(String logoName) {
		this.logoName = logoName;
	}
}
